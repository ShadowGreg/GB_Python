def sum(first_num: int, second_num: int):
    return first_num + second_num


def dif(first_num: int, second_num: int):
    return first_num - second_num


def mult(first_num: int, second_num: int):
    return first_num * second_num


def div(first_num: int, second_num: int):
    return first_num / second_num


def deg(first_num: int, second_num: int):
    return first_num ** second_num
